    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        BD bd = new BD();
        Deletar deletar = new Deletar();
        
        String id = jTextField1.getText();
        String CPF = jTextField4.getText();
        String preferencia1 = jTextField5.getText();
        String preferencia2 = jTextField6.getText();
        String preferencia3 = jTextField7.getText();
        
        bd.id = id;
        bd.CPF = CPF;
        
        deletar.apagarColaboradora();
        if(preferencia1.equals("X - Tudo")){
            Deletar.apagarColaboradora1();  
            JOptionPane.showMessageDialog(null, "Colaboradora Excluida com sucesso");
        }
        if(preferencia2.equals("X - Salada")){
            Deletar.apagarColaboradora2();
            JOptionPane.showMessageDialog(null, "Colaboradora Excluida com sucesso");
        }
        if(preferencia3.equals("X - Burguer")){
            Deletar.apagarColaboradora3();
             JOptionPane.showMessageDialog(null, "Colaboradora Excluida com sucesso");
            
        }
        
              
    }//GEN-LAST:event_jButton2ActionPerformed

